@Composable
fun LocalDemoTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    themeType: Int,
    // Dynamic color is available on Android 12+
    content: @Composable () -> Unit
) {
    val context = LocalContext.current


    val useDynamicColors = Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
    val colorScheme = when{
        themeType == 0 && useDynamicColors -> dynamicDarkColorScheme(LocalContext.current)
        themeType == 1 && useDynamicColors -> dynamicLightColorScheme(LocalContext.current)
        themeType == 2 -> DarkColorScheme
        themeType == 3 -> LightColorScheme
        themeType == 4 -> BlueColorScheme
        else -> LightColorScheme
    }
    /*val colorScheme = when {
        useDynamicColors && darkTheme ->{
            dynamicDarkColorScheme(LocalContext.current)
        }
        useDynamicColors && !darkTheme -> {
            dynamicLightColorScheme(LocalContext.current)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }*/

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content,
        shapes = Shapes
    )
}